// Complete the lonelyinteger function below.
int lonelyinteger(int a_count, int* a) {
    int aux,i,j;
    for(i = 1; i < a_count; i++){
        aux = a[i];
        for(j = i-1; a[j] > aux; j--)
            a[j+1] = a[j];
        a[j+1] = aux;
    }
    for(i = 0; i < a_count; i++){
        if(a[i] == a[i+1])
            i++;
        else
            return a[i];
    }
    return 0;
}

